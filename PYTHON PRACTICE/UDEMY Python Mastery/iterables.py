user = {
    'name': 'Sudai',
    'age': 105,
    'can_swim': True
}

for item in user.items():
    print(item)

print('\n')

for item in user.keys():
    print(item)
    
print('\n')

for item in user.values():
    print(item)
    
print('\n')

for key, value in user.items():
    print(key, value)
